package br.com.casamagalhaes.panamah.sdk.nfe;

public class InfNFe {

	private Ide ide;
	private Emit emit;
	private Total total;
	private Transp transp;
	private Pag pag;

	public Ide getIde() {
		return ide;
	}

	public void setIde(Ide ide) {
		this.ide = ide;
	}

	public Emit getEmit() {
		return emit;
	}

	public void setEmit(Emit emit) {
		this.emit = emit;
	}

	public Total getTotal() {
		return total;
	}

	public void setTotal(Total total) {
		this.total = total;
	}

	public Transp getTransp() {
		return transp;
	}

	public void setTransp(Transp transp) {
		this.transp = transp;
	}

	public Pag getPag() {
		return pag;
	}

	public void setPag(Pag pag) {
		this.pag = pag;
	}

}
